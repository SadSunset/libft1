#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*s1;

	s1 = (unsigned char *)s;
	while (n--)
	{
		if (*(s1) == (unsigned char)c)
			return ((void *)s1);
		s1++;
	}
	return (NULL);
}
